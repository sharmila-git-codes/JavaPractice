package com.java.Assignment_Nov19;

public class DatatypesDemo {
	public static void main(String[] args) {
		byte studentAge = 20;
		short studentMarks = 98;
		int studentFees = 50000;
		long studentContactNumber = 123456789;

		System.out.println("Byte: " + studentAge);
		System.out.println("Short: " + studentMarks);
		System.out.println("Int: " + studentFees);
		System.out.println("long: " + studentContactNumber);

		float studentHeight = 5.6f;
		double employeeSalary = 100000.65;
		System.out.println("Float: " + studentHeight);
		System.out.println("long: " + employeeSalary);
		
		char studentGrade = 'A';
		String studentName = "SHARMILA";
		System.out.println("char: "+studentGrade);
		System.out.println("String: "+studentName);
	}

}
